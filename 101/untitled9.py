def add(x,y):
    '''function add adds 2 numbers and prints solution'''
    solution = x + y
    return solution

def subtract(x,y):
    '''function subtracts 2 numbers and prints solution'''
    solution = x - y
    return solution

def multiply(x,y):
    '''function mulitplies 2 numbers and prints solution'''
    solution = x * y
    return solution

def divide(x,y):
    '''function divides 2 numbers and prints solution'''
    solution = x / y
    return solution